strings_array = ["joe", "mike", "amy", "sarah"]
# this method will remove the last element of the strings array
strings_array.pop

integers_array = [1, 4, 55, 29]
# this method will push the element 7 to the end of the integers array
integers_array.push(7)

floats_array = [12.1, 33.07, 9.2, 44.44]
# this method will remove the first element (12.1) of the floats array
floats_array.shift

booleans_array = [false, true, false, true]
# this method will add the boolean element "true" to the front of the booleans array
booleans_array.unshift(true)

# the first index position (0) in the strings_array is "joe"
# the last index position (3) in the strings_array is "sarah"
